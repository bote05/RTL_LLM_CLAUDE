#!/usr/bin/env python3
"""gen_dw_engine_iso_cfg.py — emit tb/engine_iso_cfg.h for a DW-ENGINE P1
depthwise dispatch (node_conv_896/902/908) so tb/engine_iso_wrap_mbv2.v +
tb/engine_iso_wrap_mbv2_tb.cpp can run the REAL engine in DEPTHWISE mode
against the per-module golden (WLAT=2 deployment URAM latency).

Usage: python scripts/gen_dw_engine_iso_cfg.py <896|902|908> [vec]
"""
from __future__ import annotations

import sys
from pathlib import Path

REPO = Path(__file__).resolve().parent.parent

BASES = {  # conv -> (weight_base_word, bias/scale base_word)
    "896": (13152, 58),
    "902": (13188, 62),
    "908": (13224, 66),
}


def main() -> int:
    conv = sys.argv[1] if len(sys.argv) > 1 else "896"
    vec = int(sys.argv[2]) if len(sys.argv) > 2 else 0
    if conv not in BASES:
        raise SystemExit(f"unknown DW conv {conv}; want one of {sorted(BASES)}")
    wbase, bbase = BASES[conv]
    mid = f"node_conv_{conv}"
    hdr = f"""// AUTO-GENERATED by scripts/gen_dw_engine_iso_cfg.py {conv} {vec} — do not edit.
// [DW-ENGINE P1] depthwise engine-isolation config for {mid}
// (C=960 3x3 s1 p1, 7x7, 4 oc_passes, K_TOTAL=9; bases from
//  scripts/extend_mbv2_engine_maps_dw.py + the patched scheduler).
#define CFG_MODULE_ID "{mid}"
#define CFG_VEC {vec}
#define CFG_GOLDIN  "output/mobilenet-v2/goldens/{mid}.goldin"
#define CFG_GOLDOUT "output/mobilenet-v2/goldens/{mid}.goldout"
#define CFG_IC 960
#define CFG_OC 960
#define CFG_KH 3
#define CFG_KW 3
#define CFG_SH 1
#define CFG_SW 1
#define CFG_PH 1
#define CFG_PW 1
#define CFG_IH 7
#define CFG_IW 7
#define CFG_OH 7
#define CFG_OW 7
#define CFG_WEIGHT_BASE {wbase}
#define CFG_BIAS_BASE {bbase}
#define CFG_SCALE_MULT 0
#define CFG_SCALE_SHIFT 0
#define CFG_ZP 0
#define CFG_ACT_IN_BASE 0
#define CFG_ACT_OUT_BASE 4096
#define CFG_IC_CHUNKS 4
#define CFG_OC_PASSES 4
#define CFG_N_IN_PIXELS 49
#define CFG_N_OUT_PIXELS 49
#define CFG_IN_BYTES_PER_SAMPLE 960
#define CFG_OUT_BYTES_PER_SAMPLE 960
#define CFG_DEPTHWISE 1
"""
    out = REPO / "tb" / "engine_iso_cfg.h"
    out.write_text(hdr, encoding="utf-8", newline="\n")
    print(f"[gen-dw-iso] wrote {out} for {mid} vec {vec} (wbase={wbase} bbase={bbase})")
    return 0


if __name__ == "__main__":
    sys.exit(main())
