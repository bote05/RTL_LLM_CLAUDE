async function main(): Promise<void> {
  await mkdir(reportsDir, { recursive: true });
  await mkdir(safeCheckpointDir, { recursive: true });

  for (const stale of ["resnet8_post_synth.dcp", "resnet8_post_opt.dcp", "resnet8_post_placed.dcp", "resnet8_post_routed.dcp"]) {
    const p = path.join(safeCheckpointDir, stale);
    if (existsSync(p)) { await rm(p, { force: true }); console.log(`[resnet8-synth] cleared stale ${stale}`); }
  }
  for (const stale of ["resnet8_util.rpt.synth", "resnet8_util.rpt.synth.hier", "resnet8_placed_util.rpt",
    "resnet8_placed_timing.rpt", "resnet8_postroute_util.rpt", "resnet8_postroute_timing.rpt"]) {
    const p = path.join(reportsDir, stale);
    if (existsSync(p)) { await rm(p, { force: true }); }
  }

  const report = await withTempDir("nn2rtl-resnet8synth-", async (tempDir) => {
    // 1) write the inactive engine+scheduler stub into the tempdir
    const stubPath = path.join(tempDir, "engine_sched_stub.v");
    await writeFile(stubPath, ENGINE_SCHED_STUB, "utf8");

    const sources = await collectSources(stubPath);
    console.log(`[resnet8-synth] collected ${sources.length} source files`);
    console.log(`[resnet8-synth] top=${topModule} part=${part} clock_ns=${clockNs} threads=${threads} synthOnly=${synthOnly}`);

    const copiedPaths: string[] = [];
    for (const src of sources) {
      const dest = path.join(tempDir, path.basename(src));
      if (src === stubPath) { copiedPaths.push(dest); continue; }
      const text = await readFile(src, "utf8");
      await writeFile(dest, convertReadmemhAbs(text, repoRoot), "utf8");
      copiedPaths.push(dest);
    }

    // 2) belt-and-suspenders: copy weight .hex/.mem into tempdir (basename-relative lookups)
    if (existsSync(weightsDir)) {
      const mems = (await readdir(weightsDir)).filter((f) => f.endsWith(".mem") || f.endsWith(".hex"));
      for (const m of mems) await copyFile(path.join(weightsDir, m), path.join(tempDir, m));
      console.log(`[resnet8-synth] copied ${mems.length} weight .mem/.hex into tempdir`);
    }

    // 3) The INERT engine path references uram_weights_bank*.mem / bias.mem / scale.mem that
    //    don't exist (the URAM banks are DEPTH(0) degenerate, never read). Generate tiny
    //    zero-init placeholders in BOTH output/resnet8/weights/ (the rewritten absolute path)
    //    and the tempdir so neither XPM MEMORY_INIT_FILE nor $readmemh aborts synth. Inert =>
    //    byte-exactness preserved; opt_design prunes the unread banks.
    const placeholderMems = [
      "uram_weights_bank0.mem", "uram_weights_bank1.mem", "uram_weights_bank2.mem", "uram_weights_bank3.mem",
      "uram_weights_bank4.mem", "uram_weights_bank5.mem", "uram_weights_bank6.mem", "uram_weights_bank7.mem",
      "bias.mem", "scale.mem",
    ];
    for (const m of placeholderMems) {
      const live = path.join(weightsDir, m);
      if (!existsSync(live)) { await writeFile(live, "0\n", "utf8"); console.log(`[resnet8-synth] generated zero-init placeholder ${m}`); }
      await copyFile(live, path.join(tempDir, m)).catch(() => {});
    }

    const utilReportPath = path.join(tempDir, "resnet8_util.rpt");
    const timingReportPath = path.join(tempDir, "resnet8_timing.rpt");
    const checkpointPath = path.join(tempDir, "resnet8.dcp");
    const tclPath = path.join(tempDir, "resnet8_synth.tcl");
    await writeFile(tclPath, buildTcl({ verilogPaths: copiedPaths, utilReportPath, timingReportPath, checkpointPath }), "utf8");

    const vivadoBin = resolveVivadoCommand(process.env);
    const vivadoArgs = ["-mode", "batch", "-source", toVivadoPath(tclPath), "-notrace"];
    const isWindowsBatch = process.platform === "win32" && /\.(bat|cmd)$/i.test(vivadoBin);
    const spawnFile = isWindowsBatch ? "cmd.exe" : vivadoBin;
    const spawnArgs = isWindowsBatch ? ["/c", vivadoBin, ...vivadoArgs] : vivadoArgs;
    console.log(`[resnet8-synth] launching: ${spawnFile} ${spawnArgs.join(" ")}`);

    const timeoutMs = (() => {
      const e = process.env.NN2RTL_VIVADO_TIMEOUT_MS;
      return e && Number.isFinite(Number(e)) && Number(e) > 0 ? Number(e) : VIVADO_TIMEOUT_MS;
    })();
    console.log(`[resnet8-synth] timeout=${(timeoutMs / 3600000).toFixed(1)}h`);

    const dcpMap = [
      { src: checkpointPath.replace(/\.dcp$/, "_synth.dcp"), dst: "resnet8_post_synth.dcp" },
      { src: checkpointPath.replace(/\.dcp$/, "_opt.dcp"), dst: "resnet8_post_opt.dcp" },
      { src: checkpointPath.replace(/\.dcp$/, "_placed.dcp"), dst: "resnet8_post_placed.dcp" },
      { src: checkpointPath, dst: "resnet8_post_routed.dcp" },
    ];
    const reportNames = ["resnet8_util.rpt.synth", "resnet8_util.rpt.synth.hier", "resnet8_placed_util.rpt",
      "resnet8_placed_timing.rpt", "resnet8_postroute_util.rpt", "resnet8_postroute_timing.rpt",
      "resnet8_postroute_power.rpt", "vivado.log", "vivado.jou"];
    let polling = true;
    const persist = async (force = false) => {
      for (const { src, dst } of dcpMap) {
        const d = path.join(safeCheckpointDir, dst);
        if (existsSync(src) && (force || !existsSync(d))) {
          try { const tmp = d + ".tmp"; await copyFile(src, tmp); await rename(tmp, d); console.log(`[resnet8-synth] persisted ${dst}${force ? " (final)" : ""}`); } catch { /* mid-write */ }
        }
      }
      for (const r of reportNames) {
        const s = path.join(tempDir, r), d = path.join(reportsDir, r);
        const alwaysRefresh = r === "vivado.log" || r === "vivado.jou";
        if (existsSync(s) && (alwaysRefresh || !existsSync(d))) { try { await copyFile(s, d); } catch { /* mid-write */ } }
      }
    };
    const poller = (async () => { while (polling) { await persist(); await new Promise((r) => setTimeout(r, 30000)); } })();

    const t0 = Date.now();
    let stdout = "", stderr = "", exitOk = true;
    let ramKillMsg = "";
    {
      const ramKillPct = (() => {
        const v = Number(process.env.NN2RTL_RAM_KILL_PCT);
        return Number.isFinite(v) && v > 0 && v < 100 ? v : 90;
      })();
      const totalMem = os.totalmem();
      console.log(
        `[resnet8-synth] RAM watchdog armed: kill at >= ${ramKillPct}% used ` +
          `(total ${(totalMem / 1073741824).toFixed(1)}GB, poll 4s)`,
      );
      const res = await new Promise<{ stdout: string; stderr: string; ok: boolean }>((resolve) => {
        const child = execFile(
          spawnFile,
          spawnArgs,
          { cwd: tempDir, env: process.env, timeout: timeoutMs, maxBuffer: VIVADO_MAX_BUFFER_BYTES },
          (err, soCb, seCb) => {
            clearInterval(poll);
            const so = typeof soCb === "string" ? soCb : (soCb?.toString() ?? "");
            const se = typeof seCb === "string" ? seCb : (seCb?.toString() ?? (err as Error | null)?.message ?? "");
            resolve({ stdout: so, stderr: se, ok: !err && !ramKillMsg });
          },
        );
        const poll = setInterval(() => {
          const freeGB = os.freemem() / 1073741824;
          const usedPct = (1 - os.freemem() / totalMem) * 100;
          if (usedPct >= ramKillPct && !ramKillMsg) {
            ramKillMsg =
              `[resnet8-synth][WATCHDOG] RAM ${usedPct.toFixed(1)}% used >= ${ramKillPct}% ` +
              `(free ${freeGB.toFixed(1)}GB) — KILLING Vivado tree (pid ${child.pid}); resume from latest resnet8_post_*.dcp`;
            console.error(ramKillMsg);
            clearInterval(poll);
            try { if (child.pid) execFileP("taskkill", ["/PID", String(child.pid), "/T", "/F"]).catch(() => {}); } catch { /* ignore */ }
            try { execFileP("taskkill", ["/IM", "vivado.exe", "/T", "/F"]).catch(() => {}); } catch { /* ignore */ }
          }
        }, 4000);
      });
      stdout = res.stdout; stderr = res.stderr; exitOk = res.ok;
    }
    polling = false; await poller; await persist(true);
    const elapsed = (Date.now() - t0) / 1000;
    console.log(`[resnet8-synth] vivado returned in ${elapsed.toFixed(1)}s (ok=${exitOk}${ramKillMsg ? ", RAM-KILLED" : ""})`);

    const utilReport = existsSync(utilReportPath) ? await readFile(utilReportPath, "utf8")
      : (existsSync(path.join(reportsDir, "resnet8_util.rpt.synth")) ? await readFile(path.join(reportsDir, "resnet8_util.rpt.synth"), "utf8") : "");
    const timingReport = existsSync(timingReportPath) ? await readFile(timingReportPath, "utf8") : "";
    const combined = [ramKillMsg, stdout, stderr, "--- util ---", utilReport, "--- timing ---", timingReport].filter(Boolean).join("\n");
    await writeFile(logPath, combined, "utf8");
    const parsed = parseVivadoReport(combined, clockNs, part);
    parsed.success = exitOk && parsed.success;
    return { ...parsed, elapsed_s: elapsed };
  });

  await writeFile(jsonReportPath, JSON.stringify(report, null, 2), "utf8");
  console.log(`[resnet8-synth] wrote ${path.relative(repoRoot, jsonReportPath)}`);
  const r = report as VivadoSynthesisReport & { elapsed_s: number };
  console.log(`[resnet8-synth] success=${report.success} LUT=${report.lut_count} FF=${report.ff_count} DSP=${report.dsp_count} BRAM36=${report.bram36_count} WNS=${report.setup_wns_ns ?? "n/a"} elapsed=${r.elapsed_s.toFixed(0)}s`);
  if (!report.success) { console.error("[resnet8-synth] FAILED/incomplete — see log + checkpoints"); process.exit(2); }
}

main().catch((err) => {
  console.error("[resnet8-synth] FATAL:", err instanceof Error ? (err.stack ?? err.message) : String(err));
  process.exit(1);
});
