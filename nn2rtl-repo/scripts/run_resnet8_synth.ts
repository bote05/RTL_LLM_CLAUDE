// ResNet-8 ENGINE-LESS (all-spatial) Vivado synth + full impl driver.
// Cloned from scripts/run_mbv2_synth.ts and adapted for the ZCU104 (xczu7ev),
// targeting the SAME part FINN used so the 3-way comparison row is apples-to-apples.
//
// Synthesises the byte-exact (e2e gate 8/8, 2,541,691 cyc) ResNet-8 top — the
// EXACT same source set that scripts/run_resnet8_top_value.ts (the Verilator e2e)
// compiles:
//   - output/resnet8/rtl/nn2rtl_top.v  (module nn2rtl_top — the ENGINE-LESS wrapper;
//     skip_fifo / frame_gate_fifo / uram_weight_bank / act_unified_mem / bias_mem /
//     engine_output_fifo / engine_output_bridge live INLINE inside this file)
//   - every per-layer module output/resnet8/rtl/node_*.v (21 of them)
//   - rtl_library/{conv_datapath,conv_datapath_parallel,conv_datapath_mp_k,
//                  coord_scheduler,line_buf_window,retile_bridge}.v
//   - a GENERATED INACTIVE stub for nn2rtl_scheduler + shared_engine (IDENTICAL to
//     the e2e build's ENGINE_SCHED_STUB) — NOT output/rtl/{nn2rtl_scheduler,
//     shared_engine_skeleton}.v (those are the ResNet-50/MBV2-modified shared files;
//     using them would change behaviour AND pull in the engine sub-blocks). The stub
//     ties engine_busy=0/spatial_stall=0 so the spatial chain runs the whole frame.
//
// $readmemh paths are rewritten to absolute so Vivado finds the weight .hex.
// The INERT engine path's MEM_INIT_FILE refs (uram_weights_bank*.mem / bias.mem /
// scale.mem) point at files that don't exist (the banks are DEPTH(0) degenerate, never
// read). We generate tiny zero-init placeholders for them so synth never aborts on a
// missing init file — byte-exactness is preserved (inert, unread, opt_design prunes).
//
// FLOW: synth_design -> opt_design -> place_design -> phys_opt_design -> route_design
//       -> report_utilization + report_timing_summary (post-route). ResNet-8 is tiny
//       (~FINN's 25K LUT class) so full impl is fast (~15-40 min).
//
// Per-stage checkpoints persist to output/resnet8/reports/synth/checkpoints/ so a
// timeout/kill is resumable.
//
// Usage:
//   NN2RTL_VIVADO_BIN=D:/vivado/2025.2/Vivado/bin/vivado.bat \
//   NN2RTL_RAM_KILL_PCT=90 \
//   npx tsx scripts/run_resnet8_synth.ts [--part=xczu7ev-ffvc1156-2-e] \
//        [--clock-ns=10] [--threads=8] [--synth-only]

